#include <stdio.h>

int main()
{
    // 정수 Integers
    int a = 1;

    // 실수 Real numbers, 부동 소수점 floating point 표현법 사용
    float f = 0.33333f;
    double d = 3.141592;
    // 3.14 = 3.14E0 = 3.14e0 = 0.314E1 = 31.4E-1

    // unsigned 부호 없는 정수 8/32비트 사용
    unsigned int aa = 2;

    // signed 부호 있는 정수 부호 1비트 + 7/31비트 사용
    signed int aaa = 3;
}